/**
 * Logique commune de sélection des attractions
 */
export function selectAttractionsFromList(
  attractions: Attraction[],
  availableMinutes: number,
  preferences: {
    types: ActivityType[];
    mustSeeQuery?: string;
    prioritizeMustSee?: boolean;
    maxPerDay?: number;
    lastCoords?: { lat: number; lng: number }; // For proximity scoring
  }
): Attraction[] {
  if (attractions.length === 0) {
    return [];
  }

  // Filtrer par mustSee si spécifié
  if (preferences.mustSeeQuery && preferences.mustSeeQuery.trim()) {
    const keywords = preferences.mustSeeQuery.toLowerCase().split(/[,;]+/).map(k => k.trim());
    const mustSeeMatches = attractions.filter(a => {
      const name = a.name.toLowerCase();
      return keywords.some(k => name.includes(k) || k.includes(name.split(' ')[0]));
    });

    if (mustSeeMatches.length > 0) {
      // Mettre les correspondances en premier
      attractions = [
        ...mustSeeMatches,
        ...attractions.filter(a => !mustSeeMatches.includes(a)),
      ];
    }
  }

  // Trier par priorité avec un système de scoring amélioré
  attractions.sort((a, b) => {
    let scoreA = 0;
    let scoreB = 0;

    // 1. +50 points si le type matche les préférences de l'utilisateur
    // C'est le critère le plus important pour respecter les choix utilisateur
    if (preferences.types.includes(a.type)) scoreA += 50;
    if (preferences.types.includes(b.type)) scoreB += 50;

    // 2. +30 points si c'est un incontournable (mustSee)
    if (preferences.prioritizeMustSee !== false) {
      if (a.mustSee) scoreA += 30;
      if (b.mustSee) scoreB += 30;
    }

    // 3. +0-10 points basés sur le rating (note sur 5 → score sur 10)
    scoreA += a.rating * 2;
    scoreB += b.rating * 2;

    // 4. +2 points si gratuit (reduced from 5 to avoid favoring unknown free attractions)
    if (a.estimatedCost === 0) scoreA += 2;
    if (b.estimatedCost === 0) scoreB += 2;

    // 5. Fame bonus based on review count (more reviews = more famous/important)
    // Max +15 points at 5000+ reviews
    const reviewBonusA = Math.min(15, (a.reviewCount || 0) / 350);
    const reviewBonusB = Math.min(15, (b.reviewCount || 0) / 350);
    scoreA += reviewBonusA;
    scoreB += reviewBonusB;

    // 6. Proximity scoring if lastCoords provided (max +20 points for < 1km)
    if (preferences.lastCoords && a.latitude && a.longitude && b.latitude && b.longitude) {
      const distA = calculateDistance(
        preferences.lastCoords.lat, preferences.lastCoords.lng,
        a.latitude, a.longitude
      );
      const distB = calculateDistance(
        preferences.lastCoords.lat, preferences.lastCoords.lng,
        b.latitude, b.longitude
      );
      // Closer = higher score (max 20 points at 0km, 0 points at 5km+)
      scoreA += Math.max(0, 20 - distA * 4);
      scoreB += Math.max(0, 20 - distB * 4);
    }

    // Tri décroissant (meilleur score en premier)
    return scoreB - scoreA;
  });

  console.log(`[Attractions] Triées par score - Top 3: ${attractions.slice(0, 3).map(a => `${a.name} (type=${a.type}, mustSee=${a.mustSee})`).join(', ')}`);

  // Sélectionner selon le temps disponible avec temps de trajet réaliste
  const selected: Attraction[] = [];
  let totalDuration = 0;
  const maxAttractions = preferences.maxPerDay || 6; // Max total (environ 3 par jour pour 2 jours)

  for (const attraction of attractions) {
    if (selected.length >= maxAttractions) break;

    // Calculer le temps de trajet depuis la dernière attraction
    const travelTime = selected.length > 0
      ? estimateTravelTime(selected[selected.length - 1], attraction)
      : 0;

    const neededTime = attraction.duration + travelTime;

    if (totalDuration + neededTime <= availableMinutes) {
      selected.push(attraction);
      totalDuration += neededTime;
    }
  }

  return selected;
}

